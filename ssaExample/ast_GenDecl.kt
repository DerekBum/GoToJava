package GoToJava

import java.io.BufferedReader
class ast_GenDecl {

	var Doc: ast_CommentGroup? = null
	var TokPos: Long? = null
	var Tok: Long? = null
	var Lparen: Long? = null
	var Specs: List<Any>? = null
	var Rparen: Long? = null
}

fun read_ast_GenDecl(buffReader: BufferedReader, id: Int): ast_GenDecl {
	val res = ast_GenDecl()
    if (id != -1) {
        if (ptrMap.containsKey(id)) {
            return ptrMap[id] as ast_GenDecl
        }
        ptrMap[id] = res
    }
    var line: String
    var split: List<String>
    var id: Int
    var readType: String

	line = buffReader.readLine()
	if (line == "end") {
        return res
    }
